import 'package:flutter/material.dart';

class Appointment {
  final int id;
  final String title;
  final String? location;
  final DateTime dateTime;

  Appointment({
    required this.id,
    required this.title,
    this.location,
    required this.dateTime,
  });

  factory Appointment.fromJson(Map<String, dynamic> json) {
    return Appointment(
      id: json['id'],
      title: json['title'],
      location: json['location'],
      dateTime: DateTime.parse(json['appointment_datetime']),
    );
  }
}
